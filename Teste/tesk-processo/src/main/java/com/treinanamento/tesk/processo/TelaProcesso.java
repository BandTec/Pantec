     private void OrganizarTabela() {

    
        
        

        DefaultTableModel modelo = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {

                return column == 0;
            }

            public Class<?> getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return Boolean.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return String.class;

                    default:
                        return String.class;
                }
            }
        };

        modelo.addColumn("Selecionar");
        modelo.addColumn("Status");
        modelo.addColumn("ProcessoID");
        modelo.addColumn("Nome do Processo");
        jTable1.setModel(modelo);
        atualizarValores();
 
      
        jTable1.setAutoCreateRowSorter(true);

    }
     
       private static void kill(String pid) throws IOException {
        String os = System.getProperty("os.name");

        if (os.contains(("Windows"))) {
            Runtime.getRuntime().exec("taskkill  /PID " + pid + "  /F");

        } else {
            Runtime.getRuntime().exec("kill -9 " + pid + " -f");

        }
    }

    private void atualizarValores(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            Teste = si.getOperatingSystem().getProcesses();
               if (Teste.isEmpty()) {
            modelo.addRow(new Object[]{false, "sem informações", "sem informações", "sem informações"});
        } else {
            for (int i = 0; i < Teste.size(); i++) {
                String id = Teste.get(i).toString().replaceFirst("[O].*?[=]", "");
                String nome = Teste.get(i).toString().replaceFirst("[O].*?[,]", "");
                String nome_processo = nome.replaceFirst("[n].*?[=]", "");

                modelo.addRow(new Object[0]);
                modelo.setValueAt(false, i, 0);
                modelo.setValueAt("Em andamento", i, 1);
                modelo.setValueAt(id.split(",")[0], i, 2);
                modelo.setValueAt(nome_processo.replaceFirst("]", ""), i, 3);

            }
        }

        
    }
