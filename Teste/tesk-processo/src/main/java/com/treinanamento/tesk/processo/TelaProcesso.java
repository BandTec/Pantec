    private void btnMatarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMatarActionPerformed
        // TODO add your handling code here:
              ArrayList<String> processos = new ArrayList();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            Boolean checked = Boolean.valueOf(jTable1.getValueAt(i, 0).toString());
            String id = jTable1.getValueAt(i, 2).toString();
            String nome_processor = jTable1.getValueAt(i, 3).toString();

            //DISPLAY
            if (checked) {

                try {
                    kill(id);
                    jTable1.setValueAt("Encerrado", i, 1);
                    processos.add(nome_processor);
                } catch (IOException ex) {
                    Logger.getLogger(TelaProcesso.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

        }
        
        if(processos.size() == 0){
               JOptionPane.showMessageDialog(null, "Escolha os processos para serem encerrados!");
        }else{
            
        String mensagem = "Processo " + processos + " interrompido com sucesso";

        JOptionPane.showMessageDialog(null, mensagem);
            
        }

     
    }//GEN-LAST:event_btnMatarActionPerformed

